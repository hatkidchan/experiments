static Font LoadFont_IBM(void)
{
    Font font = { 0 };

    font.baseSize = 8;
    font.glyphCount = 1024;
    font.glyphPadding = 4;

    // Custom font loading
    // NOTE: Compressed font image data (DEFLATE), it requires DecompressData() function
    int fontDataSize_IBM = 0;
    unsigned char *data = DecompressData(fontData_IBM, COMPRESSED_DATA_SIZE_FONT_IBMFONT, &fontDataSize_IBM);
    Image imFont = { data, 1024, 1024, 1, 2 };

    // Load texture from image
    font.texture = LoadTextureFromImage(imFont);
    UnloadImage(imFont);  // Uncompressed data can be unloaded from memory

    // Assign glyph recs and info data directly
    // WARNING: This font data must not be unloaded
    font.recs = fontRecs_IBM;
    font.glyphs = fontGlyphs_IBM;

    return font;
}
